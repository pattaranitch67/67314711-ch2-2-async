Future<String> getUserName(){
  return Future.delayed(Duration(seconds: 3), () => '2. โหลดเสร็จ');
}
void main(){
  print('1. เริ่ม');
  getUserName().then((value) => print(value));
  print('3. จบ');
}

// เดาคำตอบว่าเป็น 1. เริ่ม 
//               3. จบ
//               2. โหลดเสร็จ
//เพราะโปรแกรมจะรัน 1. เริ่มและรัน 3. จบจะมาก่อน 2. โหลดเสร็จเพราะต้องรอเรียกฟังก์ชัน 3 วินาทีซึ่งโปรแกรมจะไม่รอและรันต่อไปจึงทำให้1. เริ่ม 3. จบ มาก่อน 2. โหลดเสร็จ